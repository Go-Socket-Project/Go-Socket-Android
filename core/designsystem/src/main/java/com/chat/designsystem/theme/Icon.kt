@Composable
fun IcProfile(
    modifier: Modifier = Modifier,
    contentDescription: String = "IcLogo",
    tint: Color = Color.Unspecified,
    isState: String = "Offline"
) {
    Icon(
        modifier = modifier,
        painter = painterResource(
            id = when (isState) {
                "Offline" -> {
                    R.drawable.icon_gray_profile
                }
                "Online" -> {
                    R.drawable.icon_green_profile
                }
                else -> {
                    R.drawable.icon_red_profile
                }
            }
        ),
        contentDescription = contentDescription,
        tint = tint
    )
}
